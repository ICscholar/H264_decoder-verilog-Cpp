int sdl_event()
{
	SDL_Event event;
		/* Check for events */
	int pause;
	pause = 0;
	do
	{
 		if ( SDL_PollEvent(&event) ) 
		{
			switch (event.type)
			{
				case SDL_KEYDOWN:
					pause = 1 - pause;
					break;
				case SDL_QUIT :
					return 1;
				default:;
			}
		}
	}while(pause == 1);
	return 0;
}


void Point(int x, int y, int type,int color)
{
	char *p;
	int shift;
	if ( x < 0 || y < 0 || x >= SCREEN_WIDTH || y >= SCREEN_HEIGHT )
		return;
	p = (char *)screen->pixels + ((y*SCREEN_WIDTH + x) << 2);
	if (type == 0)
	{
		*p++ = color & 0xff;
		*p++ = (color & 0xff00) >> 8;
		*p++ = (color & 0xff0000) >> 16;
		*p = (color & 0xff000000) >> 24;
	}
	else if (type == 1) 
	{ 
		*p++ = 0x00;
		*p++ = 0x00;
		*p++ = 0x00;
		*p = 0xff;
	}
	else if (type == 2)
	{
		*p++ = 0xff - *p;
		*p++ = 0xff - *p;
		*p++ = 0xff - *p;
		*p = 0xff;
	}
}


void PointYUV(int pos_x, int pos_y, int y, int u, int v)
{
	char *p;
	int r_tmp;
	int g_tmp;
	int b_tmp;
	if ( pos_x < 0 || pos_y < 0 || pos_x >= screen->w || pos_y >= screen->h )
		return;
	p = (char *)screen->pixels + ((pos_y*screen->w + pos_x) << 2);
	r_tmp = ( y*596 + v*817 - 114131 ) >>9;
    g_tmp = ( y*596 - u*200 - v*416 + 69370) >>9;
    b_tmp = ( y*596 + u*1033 - 141787 ) >>9;
	*p++ = b_tmp < 0 ? 0 :(b_tmp > 255 ? 255 : (char)b_tmp);
	*p++ = g_tmp < 0 ? 0 :(g_tmp > 255 ? 255 : (char)g_tmp);
	*p++ = r_tmp < 0 ? 0 :(r_tmp > 255 ? 255 : (char)r_tmp);
	*p = 0xff;
}

void WriteBlockYUV(int x,int y,int width,int height, short **data_y, short **data_u, short **data_v)
{
	int x_count,y_count;
	for (y_count = 0 ; y_count < height ; y_count++)
	{
		for (x_count = 0 ; x_count < width ; x_count++)			
			PointYUV(x + x_count, y + y_count, data_y[y_count][x_count], data_u[y_count/2][x_count/2], data_v[y_count/2][x_count/2]);
	}
}

void Block(int x0, int y0, int x1, int y1,int type, int color)
{
    int   i, j;

    if (x0 > x1)
    {   
        i = x0;
        x0 = x1;
        x1 = i;
    }
    if (y0 > y1)
    {   
        i = y0;
        y0 = y1;
        y1 = i;
    }

    for(i = y0; i < y1; i++)
    {
        for(j = x0; j < x1; j++)
        {
           Point(j, i, type, color);
        }
    }
}

