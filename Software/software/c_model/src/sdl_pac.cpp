static void drawTextInternal(char *pixels, int pitch, int x, int y, const char *string, char trans, int color)
{
	char c;
	char *scr;

	int h, w;
	int *s;

	pixels += y*pitch + x*4;
	while(*string)
	{
		c = *string++;
		scr = pixels;
		s = (int *)scr;
		for (h = 0; h < 8; h++) 
		{
			for (w = 0; w < 8; w++, s++) 
			{
				if((font_data[(c<<3)+h]>>w)&1)
					*s = color;
			}
			scr += pitch;
			s = (int *)scr;
		}
		pixels += 32;
	}  
}


